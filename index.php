        <h1> Study From Home </h1>
        <hr>
        <p>
          Ada beberapa kegiatan menarik yang saya biasa dilakukan saat kondisi Work From Home atau Study From Home seperti ini dan bisa kalian coba juga:
        </p>
        <h3>1. Mencoba memasak resep makanan yang populer di media sosial</h3>
        <img src="masak.jpg">
        <p>Kegiatan ini selain bermanfaat untuk menambah skill memasakmu, juga dianggap sangat menyenangkan loh. Apalagi jika ternyata hasil masakanmu dipuji oleh orang rumah, karena cita rasanya yang menggugah selera.
        Bila sebelumnya kamu hanya mempelajari cara memasak nasi goreng ataupun tumis sayur. Sekarang manfaatkan momen #dirumahaja dengan mempelajari resep baru yang banyak beredar di TikTok. Beberapa diantaranya seperti pie susu teflon, churros, roti goreng cokelat, dan dessert box oreo.</p>
        <h3>2. Menonton film favorit atau drama yang sedang populer</h3>
        <img src="film.png">
        <p>Kamu bisa memanfaatkan layanan streaming film, mulai dari Netflix, Viu, hingga HOOQ. Salah satu kegiatan asyik yang bikin WFH jadi menyenangkan ini, bisa kamu lakukan di sela waktu istirahat siang ataupun saat tugas kuliah telah diselesaikan. Agar terkesan seperti sedang berada di bioskop, siapkan kudapan ringan untuk menemani aktivitasmu yang satu ini.</p>
        <h3>3. Berolahraga ringan</h3>
        <img src="olahraga.jpeg">
        <p>Kegiatan asyik yang bikin WFH jadi menyenangkan selanjutnya adalah dengan melakukan olahraga ringan. Selain menyehatkan, kegiatan ini juga sangat dianjurkan untuk kamu yang ingin memiliki bentuk tubuh ideal. Kegiatan ini membuat tubuh tetap bugar karena kurangnya aktivitas saat WFH seperti ini bisa membuat tubuh kita mudah lelah karena jarang bergerak atau aktivitas seperti hari-hari normal.</p>
        <h3>4. Bermain Game</h3>
        <img src="game.jpg">
        <p>Kegiatan asyik yang bikin WFH jadi menyenangkan selanjutnya adalah dengan bermain game offline maupun online. Kegiatan ini sangat sangat sering saya lakukan saat liburan antara semester, karena bermain game online bersama beberapa teman dapat sangat menyenangkan dan sejenak melepaskan penat yang ada karena #dirumahaja. Kegiatan ini boleh dilakukan saat sedang WFH atau Study From Home dengan syarat pekerjaan yang harus diselesaikan sudah terlaksana agar tidak mengganggu pekerjaan atau perkuliahan. Secara keseluruhan kegiatan bermain game ini tentunya sangat menyenangkan untuk dilakukan di masa WFH ini.</p>
